def logic(a: bytes, b: bytes, c: bytes) -> bytes:
    result = bytearray()
    prev = 0x5A

    for pa, pb, pc in zip(a, b, c):
        t1 = (pa & pb) ^ (pb | pc) ^ (pa & pc)
        t2 = t1 ^ pa ^ pb ^ pc
        t3 = t2 ^ prev
        out = ((t3 << 3) | (t3 >> 5)) & 0xFF
        result.append(out)
        prev = out

    return bytes(result)

